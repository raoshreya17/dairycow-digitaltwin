    for clip_path in clips[:5]:
        frames = read_clip(clip_path)
        if frames is None or len(frames) < 2:
            continue

        cls_idx = CLASSES.index(cls)
        print(f"Processing: {cls} → {os.path.basename(clip_path)}")

        try:
            pv      = preprocess_frames(frames)
            cam, pred_label, conf = gradcam(pv, target_class=cls_idx)

            # Get display frame
            disp_idx = min(FRAME_IDX, len(frames)-1)
            overlay  = make_overlay(frames[disp_idx], cam)

            panels.append(overlay)
            pred_labels.append(pred_label)
            confidences.append(conf)
            correct_flags.append(pred_label == cls)
            panel_added = True
            print(f"  → Predicted: {pred_label} ({conf:.1%}) ")
            break

        except Exception as e:
            print(f"  GradCAM error: {e}")
            continue

    if not panel_added:
        frames_fallback = read_clip(clips[0])
        if frames_fallback:
            disp = cv2.cvtColor(
                cv2.resize(frames_fallback[FRAME_IDX], (SIZE, SIZE)),
                cv2.COLOR_BGR2RGB)
        else:
            disp = np.zeros((SIZE, SIZE, 3), dtype=np.uint8)
        panels.append(disp)
        pred_labels.append("failed"); confidences.append(0.0)
