# ── Grad-CAM hooks
class TimeSformerGradCAM:
    def __init__(self, model):
        self.model      = model
        self.gradients  = None
        self.activations = None
        self._hooks     = []
        self._register_hooks()

    def _register_hooks(self):
        # Find last transformer encoder layer
        encoder = self.model.timesformer.encoder
        last_layer = encoder.layer[-1]

        def fwd_hook(module, inp, out):
            # out is the layer output tensor [B, N, D]
            if isinstance(out, tuple):
                self.activations = out[0].detach()
            else:
                self.activations = out.detach()

        def bwd_hook(module, grad_in, grad_out):
            if isinstance(grad_out, tuple):
                self.gradients = grad_out[0].detach()
            else:
                self.gradients = grad_out.detach()

        h1 = last_layer.register_forward_hook(fwd_hook)
        h2 = last_layer.register_full_backward_hook(bwd_hook)
        self._hooks = [h1, h2]

    def remove_hooks(self):
        for h in self._hooks:
            h.remove()

    def __call__(self, pixel_values, target_class):
        """
        pixel_values: [1, C, T, H, W] or [1, T, C, H, W]
        Returns: cam [H, W] numpy array normalised 0-1,
                 pred_label str, confidence float
        """
        self.model.eval()
        self.model.zero_grad()

        # Enable gradients
        pv = pixel_values.clone().requires_grad_(True)

        outputs = self.model(pixel_values=pv)
        logits  = outputs.logits[0]               # [num_classes]
        probs   = torch.softmax(logits, dim=0)
        pred_idx   = int(probs.argmax().item())
        confidence = float(probs[target_class].item())

        # Backprop on target class score
        score = logits[target_class]
        score.backward()

        if self.gradients is None or self.activations is None:
            print("  Warning: gradients not captured, using activations only")
            acts = self.activations          # [1, N, D]
            weights = torch.ones(acts.shape[-1], device=acts.device)
        else:
            grads   = self.gradients         # [1, N, D]
            acts    = self.activations       # [1, N, D]
            weights = grads[0].mean(dim=0)   # [D]

        # Weighted combination of activations
        cam_tokens = (acts[0] * weights.unsqueeze(0)).sum(dim=-1)  # [N]

        # Remove CLS token if present
        # TimeSformer token order: [CLS, patch_1, ..., patch_N*T]
        n_patches = (SIZE // 16) ** 2   # 196 for 224x224 with 16x16 patches
        n_frames  = NUM_FRAMES          # from config

        cam_tokens = cam_tokens[1:]     # remove CLS

        # Reshape: take spatial mean across time
        total = cam_tokens.shape[0]
        if total == n_frames * n_patches:
            cam_spatial = cam_tokens.view(n_frames, n_patches)
            cam_spatial = cam_spatial.mean(dim=0)   # [n_patches]
        else:
            # Fallback: just use all tokens
            cam_spatial = cam_tokens

        # Reshape to 2D spatial map
        h_patches = w_patches = int(math.sqrt(cam_spatial.shape[0]))
        if h_patches * w_patches != cam_spatial.shape[0]:
            h_patches = w_patches = 14  # default for 224/16

        cam_2d = cam_spatial[:h_patches*w_patches].view(h_patches, w_patches)

        # ReLU and normalise
        cam_2d = F.relu(cam_2d)
        cam_2d = cam_2d - cam_2d.min()
        cam_2d = cam_2d / (cam_2d.max() + 1e-8)

        pred_label = CLASSES[pred_idx] if pred_idx < len(CLASSES) else str(pred_idx)
        return cam_2d.cpu().numpy(), pred_label, confidence


def preprocess_frames(frames_bgr):
    frames_rgb = [cv2.cvtColor(f, cv2.COLOR_BGR2RGB) for f in frames_bgr]
    from PIL import Image
    pil_frames = [Image.fromarray(f) for f in frames_rgb]
    inputs = processor(images=pil_frames, return_tensors="pt")
    return inputs["pixel_values"].to(device)   # [1, C, T, H, W] or [1, T, C, H, W]


def read_clip(path, n_frames=None):
    if n_frames is None:
        n_frames = NUM_FRAMES
    cap = cv2.VideoCapture(path)
    all_frames = []
    while True:
        ok, frame = cap.read()
        if not ok:
            break
        all_frames.append(frame)
    cap.release()
    if not all_frames:
        return None
    # Uniform sample
    total = len(all_frames)
    idxs  = np.linspace(0, total-1, n_frames).round().astype(int)
    return [all_frames[i] for i in idxs]


def make_overlay(frame_bgr, cam_np):
    frame_resized = cv2.resize(frame_bgr, (SIZE, SIZE))
    cam_uint8 = (cam_np * 255).astype(np.uint8)
    cam_resized = cv2.resize(cam_uint8, (SIZE, SIZE))
    heat = cv2.applyColorMap(cam_resized, OVERLAY_CLR)
    overlay = cv2.addWeighted(frame_resized, 1.0 - OVERLAY_A, heat, OVERLAY_A, 0)
    return cv2.cvtColor(overlay, cv2.COLOR_BGR2RGB)


